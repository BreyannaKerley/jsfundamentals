// Assignment Operator which is this single equal sign
let r = 5;

// addition assignment operator
r += 1 // r = r + 1
console.log(r); //which equals 6

// subtraction operator
r -= 1 // r = r -1
console.log(r); // 5

// multiplication assignemnt operator
r *= 1; // r = r * 1
console.log(r) // 5

// division assignment operator
r /= 2;
console.log(r);

// remainder (modulus) assignment operator
r %= 2; // r = r % 2
console.log(r); // .5

// exponential assignment operator
r **= 1; // r = r ** 1
console.log(r);
